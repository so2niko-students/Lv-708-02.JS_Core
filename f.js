// Цельсий х 1,8 + 32 = Фаренгейт

const c = prompt('Введіть температуру в градусах Цельсія');

// Додано перевірку на порожнє значення або відміну
let text = "помилка! Не введено число";

if (!isNaN(+c) && c != ''){
    const f = c*1.8+32;
    text = `Це те саме, що ${f} по Фаренгейту`;
}

alert(text);